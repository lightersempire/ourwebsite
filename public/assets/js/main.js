// post register form using fetch API
const signupForm = document.getElementById('registerForm')

signupForm.addEventListener('submit', async function (evt) {
    evt.preventDefault(); // to prevent the custom form property of reloading on submission.

    const fname = document.getElementById('fname').value;
    const lname = document.getElementById('lname').value;
    const phone = document.getElementById('phone').value;
    const country = document.getElementById('country').value; 
    const state = document.getElementById('state').value;
    const email = document.getElementById('email').value;
    const comment = document.getElementById('comment').value;
   

    await fetch('/',{
        method: 'POST',
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({
            fname: fname,
            lname: lname,
            phone: phone,
            country: country,
            state : state,
            email: email,
            comment : comment,
        })
    })
        .then(res => res.json())
        .then(data => {
            const message = data.value;
            const emailexist = data.email;
            if (message) {
                swal({
                    icon: "success",
                    text: "Registration successful",
                    button: false,
                })
                setTimeout(() => { location.assign('/'); }, 1400);
                // setTimeout(() => { location.assign('/index'); }, 1000);
                }
            else if (emailexist){
                    swal({
                    icon: "error",
                    text: "email record is in our database",
                    button: false,
                })
                }

            else {
             swal({
                    icon: "error",
                    text: "Please try again",
                    button: false,
                })
            }
          })

        })

